DROP VIEW SYS.V_$CLIENT_RESULT_CACHE_STATS;

/* Formatted on 5/28/2010 6:43:38 PM (QP5 v5.136.908.31019) */
CREATE OR REPLACE FORCE VIEW SYS.V_$CLIENT_RESULT_CACHE_STATS
(
   "CLIENT REGID",
   BLOCK_SIZE,
   BLOCK_MAX,
   BLOCK_COUNT,
   BUCKET_COUNT,
   CREATE_SUCC,
   CREATE_FAIL,
   FINDS,
   INVALIDATIONS,
   DELETE_INVALIDS,
   DELETE_VALIDS
)
AS
   SELECT "CLIENT REGID",
          "BLOCK_SIZE",
          "BLOCK_MAX",
          "BLOCK_COUNT",
          "BUCKET_COUNT",
          "CREATE_SUCC",
          "CREATE_FAIL",
          "FINDS",
          "INVALIDATIONS",
          "DELETE_INVALIDS",
          "DELETE_VALIDS"
     FROM v$client_result_cache_stats;


DROP PUBLIC SYNONYM V$CLIENT_RESULT_CACHE_STATS;

CREATE PUBLIC SYNONYM V$CLIENT_RESULT_CACHE_STATS FOR SYS.V_$CLIENT_RESULT_CACHE_STATS;


GRANT SELECT ON SYS.V_$CLIENT_RESULT_CACHE_STATS TO SELECT_CATALOG_ROLE;
